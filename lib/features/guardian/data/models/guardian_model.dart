import 'package:qlm_mobile_suite/features/guardian/domain/entities/guardian_entity.dart';

class GuardianModel extends Guardian {
  GuardianModel({
    required super.uuid,
    super.firstName,
    super.fatherName,
    super.familyName,
    super.fullName,
    super.phoneNumber,
    super.homePhone,
    super.birthDate,
    super.birthPlace,
    super.proofType,
    super.proofNumber,
    super.issuingAuthority,
    super.issueDate,
    super.expiryDate,
    super.qualification,
    super.job,
    super.workplace,
    super.specializationAreaId,
    super.specializationAreaName,
    super.weaponLicenseNumber,
    super.weaponLicenseType,
    super.weaponLicenseExpiry,
    super.electronicCardNumber,
    super.electronicCardIssueDate,
    super.electronicCardExpiryDate,
    super.employmentStatus,
    super.stopDate,
    super.createdAt,
    super.updatedAt,
  });

  factory GuardianModel.fromJson(Map<String, dynamic> json) {
    return GuardianModel(
      uuid: json['uuid'] ?? '',
      firstName: json['first_name'],
      fatherName: json['father_name'],
      familyName: json['family_name'],
      fullName: json['full_name'],
      phoneNumber: json['phone_number'],
      homePhone: json['home_phone'],
      birthDate: json['birth_date'] != null ? DateTime.tryParse(json['birth_date']) : null,
      birthPlace: json['birth_place'],
      proofType: json['proof_type'],
      proofNumber: json['proof_number'],
      issuingAuthority: json['issuing_authority'],
      issueDate: json['issue_date'] != null ? DateTime.tryParse(json['issue_date']) : null,
      expiryDate: json['expiry_date'] != null ? DateTime.tryParse(json['expiry_date']) : null,
      qualification: json['qualification'],
      job: json['job'],
      workplace: json['workplace'],
      specializationAreaId: json['specialization_area_id'],
      specializationAreaName: json['specialization_area_name'],
      weaponLicenseNumber: json['weapon_license_number'],
      weaponLicenseType: json['weapon_license_type'],
      weaponLicenseExpiry: json['weapon_license_expiry'] != null ? DateTime.tryParse(json['weapon_license_expiry']) : null,
      electronicCardNumber: json['electronic_card_number'],
      electronicCardIssueDate: json['electronic_card_issue_date'] != null ? DateTime.tryParse(json['electronic_card_issue_date']) : null,
      electronicCardExpiryDate: json['electronic_card_expiry_date'] != null ? DateTime.tryParse(json['electronic_card_expiry_date']) : null,
      employmentStatus: json['employment_status'],
      stopDate: json['stop_date'] != null ? DateTime.tryParse(json['stop_date']) : null,
      createdAt: json['created_at'] != null ? DateTime.tryParse(json['created_at']) : null,
      updatedAt: json['updated_at'] != null ? DateTime.tryParse(json['updated_at']) : null,
    );
  }

  Map<String, dynamic> toJson() {
    return {
      'uuid': uuid,
      'first_name': firstName,
      'father_name': fatherName,
      'family_name': familyName,
      'full_name': fullName,
      'phone_number': phoneNumber,
      'home_phone': homePhone,
      'birth_date': birthDate?.toIso8601String(),
      'birth_place': birthPlace,
      'proof_type': proofType,
      'proof_number': proofNumber,
      'issuing_authority': issuingAuthority,
      'issue_date': issueDate?.toIso8601String(),
      'expiry_date': expiryDate?.toIso8601String(),
      'qualification': qualification,
      'job': job,
      'workplace': workplace,
      'specialization_area_id': specializationAreaId,
      'specialization_area_name': specializationAreaName,
      'weapon_license_number': weaponLicenseNumber,
      'weapon_license_type': weaponLicenseType,
      'weapon_license_expiry': weaponLicenseExpiry?.toIso8601String(),
      'electronic_card_number': electronicCardNumber,
      'electronic_card_issue_date': electronicCardIssueDate?.toIso8601String(),
      'electronic_card_expiry_date': electronicCardExpiryDate?.toIso8601String(),
      'employment_status': employmentStatus,
      'stop_date': stopDate?.toIso8601String(),
      'created_at': createdAt?.toIso8601String(),
      'updated_at': updatedAt?.toIso8601String(),
    };
  }
}
